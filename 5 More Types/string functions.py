#joins a list as a single string with a seperator
print('a'.join(["hi", "hello", "howdy", "hey"]))
#replaces part of a string with another string
print("What the HELL".replace("HELL","heck"))
#determines if a string starts with something
print("What's up GAMERS??".startswith("What"))
#determines if a string ends with something
print("Tyler Lee".endswith("Lee"))
#makes everything uppercase
print("I am very angry".upper())
#makes everything lowercase
print("I AM NOT ANGRY".lower())
#splits a string into a list with a seperator
print("Milk, Bread, Eggs, Avacado, Mouse".split(", "))